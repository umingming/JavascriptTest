function log() {
    for (const [key1, value1] of Object.entries(obj)) {
        if (typeof value1 === "object") {
            for (const [key2, value2] of Object.entries(value1)) {
                if (typeof value2 === "object") {
                    for (const [key3, value3] of Object.entries(value2)) {
                        // console.log(`${key1}.${key2}.${key3}`);
                        console.log(encodeResourceValue(value3));
                    }
                } else {
                    // console.log(`${key1}.${key2}`);
                    console.log(encodeResourceValue(value2));
                }
            }
        } else {
            // console.log(`${key1}`);
            console.log(encodeResourceValue(value1));
        }
    }
}

function encodeResourceValue(value) {
    let unicode = "";
    for (let i of value) {
        let charCode = i.charCodeAt(0).toString(16);
        unicode += `\\u${charCode.padStart(4, "0")}`;
    }

    return unicode;
}

