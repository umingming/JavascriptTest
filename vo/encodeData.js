function encodeValue(value) {
    let unicode = "";
    for (let i of value) {
        let charCode = i.charCodeAt(0).toString(16);
        unicode += `\\u${charCode.padStart(4, "0")}`;
    }

    return unicode;
}
function decodeValue(value) {
    let strValue = `"${value}"`;
    return JSON.parse(strValue);
}

